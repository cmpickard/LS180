-- What are the three levels of schema?
-- ANS: Conceptual, Logical, Physical

-- What is a conceptual schema?
-- ANS: It is an abstract model of the major entities and the relationships between them
-- in the problem domain

-- What is a physical schema?
-- ANS: It is a detailed, specific implementation of the problem domain, designed w/
-- some particular database software in mind. The specifics will include a list of 
-- all the tables, the schemas (including columns, datatypes, constraints, keys), 
-- and the foreign key / primary key relationships that exist between those tables

-- What are the three types of relationships that can be shown in a database diagram?
-- One-to-One
-- One-to-Many
-- Many-to-Many
